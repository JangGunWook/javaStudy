package do_while문;

public class ex01do_while문기초 {

	public static void main(String[] args) {
		
		
		// do-while문 문법
		do {
			System.out.println("반드시 한번은 실행!!");
		}while(false);
		
		// while문, do-while문
		// --> 반복횟수 명확하지 않으때 사용
		// do while문은 반드시 한번은 실행해야하는 코드가 있을 때 많이 사용
		// --> 검사조건이 false여도 한번은 실행된다!
		
		
		
		
	}

}
